# ╔═╡ e802c9be-318e-4621-97ed-772e4b5432aa
model = GmshDiscreteModel("mesh3D_v2.msh")


# ╔═╡ feb48220-169e-4938-9ed0-5ecddc275003
model.face_labeling

# ╔═╡ 23dd4830-ea80-4c82-b912-1e2339b459b7
begin
	order = 1
	
	reffe = ReferenceFE(lagrangian,VectorValue{3,Float64},order)
	V0 = TestFESpace(model,reffe;
	  conformity=:H1,
	  dirichlet_tags=["surf1","surf2"],
	  dirichlet_masks=[(true,false,false), (true,true,true)])
end

# ╔═╡ 4374c48e-25d3-427f-b318-035eb90e76db
# begin
# 	i = 1
# 	name = "results$i"
# end

# ╔═╡ e2859210-296f-4982-907d-8c1291fc10cf
begin
	const E = 70.0e9
	const ν = 0.33
	const λ = (E*ν)/((1+ν)*(1-2*ν))
	const μ = E/(2*(1+ν))
	for i in 1:10
	    g1(x) = VectorValue(0.05*i,0.0,0.0)
	    g2(x) = VectorValue(0.0,0.0,0.0)
	
	    U = TrialFESpace(V0,[g1,g2])
	
	
	    σ(ε) = λ*tr(ε)*one(ε) + 2*μ*ε
	
	
	    degree = 2*order
	    Ω = Triangulation(model)
	    dΩ = Measure(Ω,degree)
	
	    a(u,v) = ∫( ε(v) ⊙ (σ∘ε(u)) )*dΩ
	    l(v) = 0
	
	    op = AffineFEOperator(a,l,U,V0)
	    uh = solve(op)
	
	    writevtk(Ω,"results_$i",cellfields=["uh"=>uh,"epsi"=>ε(uh),"sigma"=>σ∘ε(uh)])
	end
end

